--  DDL for View CART_CHECK_VIEW
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "BITCAMP"."CART_CHECK_VIEW" ("ID", "MEMBER_ID", "NAME", "PRICE", "PRODUCT_COUNT", "TOTAL_MONEY") AS 
  SELECT C.ID,
           M.ID MEMBER_ID,
           P.NAME,
           P.PRICE,
           C.PRODUCT_COUNT,
           ( P.PRICE * C.PRODUCT_COUNT ) TOTAL_MONEY
      FROM MEMBER M,
           PRODUCT P,
           CART C
     WHERE M.ID = C.MEMBER_ID
       AND P.ID = C.PRODUCT_ID
     ORDER BY C.ID
;
