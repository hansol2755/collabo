  CREATE OR REPLACE FORCE VIEW "BOB"."CART_CHECK_VIEW" ("ID", "MEMBER_ID", "NAME", "PRICE", "PRODUCT_COUNT", "TOTAL_MONEY") AS 
  select c.id, m.id member_id, p.name, p.price, c.product_count, (p.price * c.product_count ) total_money
  from member m, product p, cart c
 where m.id = c.member_id and p.id = c.product_id and c.member_id = 'hong'
 order by c.id
;
